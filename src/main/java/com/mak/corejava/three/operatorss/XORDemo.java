package com.mak.corejava.three.operatorss;
class XORDemo 
{
	public static void main(String[] args) 
	{
		System.out.println(5 & 6);
		System.out.println(5 | 6);
		System.out.println(5 ^ 6);

		System.out.println(true & false);
		System.out.println(true | false);
		System.out.println(true ^ true);

	}
}
/*
			0101
			0110

& =>	0100 
|	=>	0111 
^	=>	0011 
*/