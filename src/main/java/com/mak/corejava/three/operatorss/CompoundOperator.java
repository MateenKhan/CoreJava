package com.mak.corejava.three.operatorss;
class CompoundOperator 
{
	public static void main(String[] args) 
	{
		int a=10;
		a+=10;
		System.out.println("a+="+a);
		a/=10;
		System.out.println("a/="+a);
		a*=10;
		System.out.println("a*="+a);
		a-=10;
		System.out.println("a-="+a);
		a%=10;
		System.out.println("a%="+a);
		
	}
}