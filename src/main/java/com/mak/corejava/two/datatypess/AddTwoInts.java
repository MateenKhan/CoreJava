package com.mak.corejava.two.datatypess;
class  AddTwoInts
{
	public static void main(String[] args) 
	{
		int a = 10;
		int b = 20;
		int c = a + b;
		System.out.println("int:"+c);
		int i1 = 10;
		float  f1 = 20;
		float f2 = i1 + f1;
		System.out.println("float:"+f2);
		byte b1 = 10; 
		byte b2 = 20; 
		byte b3 = (byte)(b1 + b2);
		System.out.println("byte:"+(byte)b3);
		
	}
}
